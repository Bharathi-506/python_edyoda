class calculator():
  def __init__(self,x,y):
    self.x=x
    self.y=y

  def add(self):
    print("Sum :",self.x+self.y)

  def subtraction(self):
    print("Subtraction :",self.y-self.x)  

  def multiplication(self):
    print("Multiplication :",self.x*self.y)

  def division(self):
    print("Division :",self.x/self.y)  

n1=calculator(10,94) 

n1.add()
n1.subtraction()
n1.multiplication()
n1.division()
        


    


     

